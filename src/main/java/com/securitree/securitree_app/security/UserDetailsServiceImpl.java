package com.securitree.securitree_app.security;

import com.securitree.securitree_app.model.User;
import com.securitree.securitree_app.repositories.UserRepository;
import java.util.Optional;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;

/**
 *
 * @author Bruce Gwandu
 */
public class UserDetailsServiceImpl implements UserDetailsService {

    @Autowired
    private UserRepository userRepository;

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        Optional<User> user = userRepository.findByUsername(username);
        User usr = user.orElseThrow(() -> new UsernameNotFoundException("Wrong Credentials!"));
        return new UserDetailsImpl(usr);
    }

}
